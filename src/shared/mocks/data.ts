function generateMetricPoints(hours: number, baseValue: number, variance: number) {
  const points = [];
  for (let i = hours; i >= 0; i--) {
    points.push({
      timestamp: hoursAgo(i),
      value: Math.round(baseValue + (Math.random() - 0.5) * variance),
    });
  }
  return points;
}

